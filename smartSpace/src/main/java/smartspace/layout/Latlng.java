package smartspace.layout;

public class Latlng {

	private Double lat;
	private Double lng;
	
	public Latlng() {	
	}
	
	public Double getLat() {
		return lat;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}

	public Double getLng() {
		return lng;
	}

	public void setLng(Double lng) {
		this.lng = lng;
	}



}
